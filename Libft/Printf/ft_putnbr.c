#include "ft_printf.h"

int	ft_putnbr(int nb)
{
	char	c;
	int		i;

	i = 0;
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return (11);
	}
	if (nb < 0)
	{
		write(1, "-", 1);
		nb *= -1;
		i = 1;
	}
	if (nb > 9)
		ft_putnbr(nb / 10);
	c = (nb % 10) + '0';
	write(1, &c, 1);
	while (nb > 9)
	{
		nb /= 10;
		i++;
	}
	return (i + 1);
}
